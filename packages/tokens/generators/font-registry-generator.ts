/**
 * Generates packages/tokens-web/src/typography/font-registry.ts.
 *
 * Exports:
 *   FontMeta / FontSource  — types
 *   fontRegistry           — full metadata map (name → FontMeta)
 *   isGoogleFont(family)   — true if loaded from Google Fonts CDN
 *   buildGoogleFontsUrl()  — <link href="..."> URL covering all google-fonts entries
 *   buildGoogleFontsCssImport() — @import rule string for stylesheet prepending
 */
export function generateWebFontRegistry(
  outDir: string,
  registry: Record<string, FontMeta>,
): void {
  const HEADER = [
    '// AUTO-GENERATED by @btech/design-system — do not edit manually.',
    '// Run `pnpm generate` to regenerate from sources/core/font-registry.json.',
    '',
  ].join('\n');

  const registryLines = Object.entries(registry).map(([name, meta]) => {
    const parts: string[] = [`source: '${meta.source}'`];
    if (meta.weights?.length)  parts.push(`weights: [${meta.weights.join(', ')}]`);
    if (meta.styles?.length)   parts.push(`styles: [${meta.styles.map(s => `'${s}'`).join(', ')}]`);
    parts.push(`fallback: '${meta.fallback.replace(/'/g, "\\'")}'`);
    if (meta.platform?.length) parts.push(`platform: [${meta.platform.map(p => `'${p}'`).join(', ')}]`);
    if (meta.url)              parts.push(`url: '${meta.url}'`);
    return `  '${name}': { ${parts.join(', ')} },`;
  });

  const L: string[] = [
    HEADER,
    "export type FontSource = 'google-fonts' | 'system' | 'asset' | 'custom-cdn';",
    '',
    'export interface FontMeta {',
    '  source: FontSource;',
    '  weights?: number[];',
    '  styles?: string[];',
    '  fallback: string;',
    '  /** Platforms where this is a system font (e.g. ["android"]). */',
    '  platform?: string[];',
    '  /** CDN URL for custom-cdn fonts. */',
    '  url?: string;',
    '}',
    '',
    '/**',
    ' * Registry of every font family used in the BTech design system.',
    ' * Generated from sources/core/font-registry.json.',
    ' *',
    ' * Use this to drive `<link>` injection, @font-face rules,',
    ' * or conditional font-loading logic in React / Vue apps.',
    ' *',
    " * @example React",
    " * import { buildGoogleFontsUrl } from '@btech/tokens';",
    " * <link rel='stylesheet' href={buildGoogleFontsUrl()} />",
    ' *',
    " * @example Vue (with @vueuse/head)",
    " * import { buildGoogleFontsUrl } from '@btech/tokens';",
    " * useHead({ link: [{ rel: 'stylesheet', href: buildGoogleFontsUrl() }] });",
    ' */',
    'export const fontRegistry: Record<string, FontMeta> = {',
    ...registryLines,
    '};',
    '',
    '/** Returns true if [family] should be loaded from the Google Fonts CDN. */',
    'export function isGoogleFont(family: string): boolean {',
    "  return fontRegistry[family]?.source === 'google-fonts';",
    '}',
    '',
    '/**',
    ' * Builds a Google Fonts CDN URL covering all `google-fonts` entries in the registry.',
    ' * Each family includes all registered weights and italic variants automatically.',
    ' */',
    'export function buildGoogleFontsUrl(): string {',
    '  const families = Object.entries(fontRegistry)',
    "    .filter(([, meta]) => meta.source === 'google-fonts')",
    '    .map(([name, meta]) => {',
    "      const weights = (meta.weights ?? [400]).join(';');",
    "      const hasItalic = meta.styles?.includes('italic') ?? false;",
    '      const encoded = encodeURIComponent(name);',
    '      return hasItalic',
    "        ? `family=${encoded}:ital,wght@0,${weights};1,${weights}`",
    "        : `family=${encoded}:wght@${weights}`;",
    '    });',
    "  return `https://fonts.googleapis.com/css2?${families.join('&')}&display=swap`;",
    '}',
    '',
    '/**',
    ' * Returns a CSS `@import` statement for all Google Fonts in the registry.',
    ' * Prepend this to your global stylesheet or inject via a `<style>` tag.',
    ' *',
    ' * @example',
    " * import { buildGoogleFontsCssImport } from '@btech/tokens';",
    " * const fullCss = buildGoogleFontsCssImport() + existingStylesheet;",
    ' */',
    'export function buildGoogleFontsCssImport(): string {',
    "  return `@import url('${buildGoogleFontsUrl()}');\\n`;",
    '}',
    '',
  ];

  writeFileSync(`${outDir}/font-registry.ts`, L.join('\n') + '\n');
}

// =============================================================================
// CSS: prepend @import to styles.css
// =============================================================================

/**
 * Prepends a Google Fonts @import to packages/tokens-web/dist/styles.css.
 * Idempotent — skips if the import is already present.
 */
export function prependGoogleFontsCssImport(
  cssPath: string,
  registry: Record<string, FontMeta>,
): void {
  const googleEntries = Object.entries(registry).filter(([, m]) => m.source === 'google-fonts');
  if (googleEntries.length === 0) return;

  const families = googleEntries.map(([name, meta]) => {
    const weights = (meta.weights ?? [400]).join(';');
    const hasItalic = meta.styles?.includes('italic') ?? false;
    const encoded = encodeURIComponent(name);
    return hasItalic
      ? `family=${encoded}:ital,wght@0,${weights};1,${weights}`
      : `family=${encoded}:wght@${weights}`;
  });

  const importLine = `@import url('https://fonts.googleapis.com/css2?${families.join('&')}&display=swap');\n\n`;

  const existing = readFileSync(cssPath, 'utf-8');
  if (existing.startsWith('@import url(')) return; // already present

  writeFileSync(cssPath, importLine + existing, 'utf-8');
  console.log(`  CSS @import  — Google Fonts prepended to styles.css (${googleEntries.length} families)`);
}
