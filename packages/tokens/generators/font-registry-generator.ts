import { readFileSync, writeFileSync, existsSync } from 'fs';
import { ROOT } from './utils.js';

// =============================================================================
// Types
// =============================================================================

export type FontSource = 'google-fonts' | 'system' | 'asset' | 'custom-cdn';

export interface FontMeta {
  $description?: string;
  source: FontSource;
  weights?: number[];
  styles?: string[];
  fallback: string;
  /** Platforms where this font is pre-installed (e.g. ["android"]). */
  platform?: string[];
  /** URL for custom-cdn fonts. */
  url?: string;
}

// =============================================================================
// Loader
// =============================================================================

export function loadFontRegistry(): Record<string, FontMeta> {
  const path = `${ROOT}/sources/core/font-registry.json`;
  if (!existsSync(path)) {
    console.warn('  [font-registry] sources/core/font-registry.json not found — skipping.');
    return {};
  }
  const raw = JSON.parse(readFileSync(path, 'utf-8')) as { fontRegistry: Record<string, FontMeta> };
  return raw.fontRegistry ?? {};
}

