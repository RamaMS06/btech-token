/**
 * Generates packages/tokens-dart/lib/src/typography/font_registry.dart.
 *
 * Exports BTechFontRegistry with:
 *   isGoogleFont(family)  — decides GoogleFonts.getFont() vs TextStyle(fontFamily:)
 *   isSystemFont(family)  — pre-installed on the OS, no download needed
 *   fallbackFor(family)   — CSS-style fallback string for informational use
 */
export function generateFlutterFontRegistry(
  outDir: string,
  registry: Record<string, FontMeta>,
): void {
  const HEADER = [
    '// AUTO-GENERATED by @btech/design-system — do not edit manually.',
    '// Run `pnpm generate` to regenerate from sources/core/font-registry.json.',
    '',
    '// ignore_for_file: lines_longer_than_80_chars',
    '',
  ].join('\n');

  const googleFonts = Object.entries(registry)
    .filter(([, m]) => m.source === 'google-fonts')
    .map(([name]) => name);

  const systemFonts = Object.entries(registry)
    .filter(([, m]) => m.source === 'system')
    .map(([name]) => name);

  const L: string[] = [
    HEADER,
    "import 'package:flutter/material.dart';",
    '',
    '/// Font source registry — generated from sources/core/font-registry.json.',
    '///',
    '/// Tells each platform how to load a font family:',
    '///   google-fonts → use GoogleFonts.getFont() (downloaded on demand)',
    '///   system       → use TextStyle(fontFamily:) (OS resolves natively)',
    '///   asset        → use TextStyle(fontFamily:) (Flutter asset pipeline)',
    '///',
    '/// ```dart',
    "/// import 'package:google_fonts/google_fonts.dart';",
    '///',
    "/// TextStyle _build(String family) =>",
    "///   BTechFontRegistry.isGoogleFont(family)",
    "///       ? GoogleFonts.getFont(family, fontSize: 16)",
    "///       : TextStyle(fontFamily: family, fontSize: 16);",
    '/// ```',
    'abstract class BTechFontRegistry {',
    '',
    '  /// Font families available via the google_fonts Flutter package.',
    '  static const Set<String> googleFonts = {',
    ...googleFonts.map(name => `    '${name}',`),
    '  };',
    '',
    '  /// Font families pre-installed on the OS — no network fetch required.',
    '  static const Set<String> systemFonts = {',
    ...systemFonts.map(name => `    '${name}',`),
    '  };',
    '',
    '  /// CSS-style fallback stacks for each registered family.',
    '  static const Map<String, String> fallbacks = {',
    ...Object.entries(registry).map(([name, m]) => `    '${name}': '${m.fallback.replace(/'/g, "\\'")}',`),
    '  };',
    '',
    '  /// Returns true if [family] can be loaded via the google_fonts package.',
    '  static bool isGoogleFont(String family) => googleFonts.contains(family);',
    '',
    '  /// Returns true if [family] is pre-installed on the OS.',
    '  static bool isSystemFont(String family) => systemFonts.contains(family);',
    '',
    "  /// Returns the CSS fallback stack for [family], or 'sans-serif' if unknown.",
    "  static String fallbackFor(String family) => fallbacks[family] ?? 'sans-serif';",
    '}',
    '',
  ];

  writeFileSync(`${outDir}/font_registry.dart`, L.join('\n') + '\n');
}

