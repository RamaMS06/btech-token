import { readFileSync, readdirSync, existsSync, writeFileSync } from 'fs';
import { ROOT, flattenDTCG, pathToCssVarStem } from '../utils.js';

/** Generate token.ts вЂ” TokenPath union type + token() and cssVar() helpers. */
export function generateTokenTypes(webSrcPath: string): void {
  const dirs = [
    `${ROOT}/sources/core`,
    `${ROOT}/sources/semantic`,
    `${ROOT}/sources/components`,
  ];

  const allPaths = new Set<string>();
  for (const dir of dirs) {
    if (!existsSync(dir)) continue;
    for (const file of readdirSync(dir)) {
      if (!file.endsWith('.json')) continue;
      const flat = flattenDTCG(JSON.parse(readFileSync(`${dir}/${file}`, 'utf-8')));
      for (const rawPath of Object.keys(flat)) {
        allPaths.add(rawPath.replace(/\.default$/, ''));
      }
    }
  }

  // Add composite typography token paths (font shorthand vars).
  // These are generated by web-generator.ts from typeScale JSON entries
  // and map to CSS custom properties like --typography-heading-h1.
  const typoJson = JSON.parse(
    readFileSync(`${ROOT}/sources/semantic/typography.json`, 'utf-8')
  ) as { typography?: { typeScale?: Record<string, Record<string, unknown>> } };
  const typeScale = typoJson.typography?.typeScale ?? {};
  for (const [category, entries] of Object.entries(typeScale)) {
    for (const name of Object.keys(entries)) {
      allPaths.add(`typography.${category}.${name}`);
    }
  }

  const sorted = [...allPaths].sort();
  const unionLines = sorted.map(p => `  | '${p}'`).join('\n');

  const out = [
    `// AUTO-GENERATED by @btech/design-system вЂ” do not edit manually.`,
    `// Run \`pnpm generate\` to regenerate from tokens/.`,
    ``,
    `/**`,
    ` * All valid BTech design token paths.`,
    ` * Grammar: [category].[property].[role].[emphasis].[state]`,
    ` *`,
    ` * @example`,
    ` * 'color.bg.primary'                вЂ” primary background color`,
    ` * 'color.text.secondary'           вЂ” secondary text color`,
    ` * 'spacing.md'                      вЂ” medium spacing (12px)`,
    ` * 'radius.sm'                       вЂ” small border radius (8px)`,
    ` */`,
    `export type TokenPath =`,
    unionLines,
    ``,
    `// =============================================================================`,
    `// CSS variable naming (no vendor prefix)`,
    `// =============================================================================`,
    `// styles.css defines every token as a CSS custom property:`,
    `//   :root {`,
    `//     --bg-primary: #...;                  (color вЂ” category dropped)`,
    `//     --space-md: 16px;                    (spacing в†’ space)`,
    `//     --typography-font-family-sans: ...;  (typography вЂ” category kept)`,
    `//     --border-neutral: #...;              (color.stroke в†’ border)`,
    `//     --duration-fast: 100ms;              (motion вЂ” category dropped)`,
    `//     --z-modal: 400;                      (zIndex в†’ z)`,
    `//   }`,
    `//`,
    `// token() returns the identical var() вЂ” same as writing it by hand in CSS.`,
    `// Dark mode via [data-mode="dark"] works automatically for both.`,
    `// =============================================================================`,
    ``,
    `// Internal: same logic as pathToCssVarStem() in config/generators/utils.ts`,
    `const PRIMITIVE_COLORS = new Set(['blue','red','green','orange','neutral','yellow','pink','purple','teal','slate']);`,
    `function _pathToVarName(dotPath: string): string {`,
    `  const [cat, ...rest] = dotPath.split('.');`,
    `  let stem: string;`,
    `  switch (cat) {`,
    `    case 'color': {`,
    `      const sub = rest[0];`,
    `      if (PRIMITIVE_COLORS.has(sub)) { stem = ['color', ...rest].join('-'); break; }`,
    `      if (sub === 'stroke')          { stem = ['border', ...rest.slice(1)].join('-'); break; }`,
    `      stem = rest.join('-'); break;`,
    `    }`,
    `    case 'spacing': stem = ['space', ...rest].join('-'); break;`,
    `    case 'typography': stem = [cat, ...rest].join('-'); break;`,
    `    case 'zIndex': stem = ['z', ...rest].join('-'); break;`,
    `    case 'motion': stem = rest.join('-'); break;`,
    `    default: stem = [cat, ...rest].join('-');`,
    `  }`,
    `  return stem.replace(/([A-Z])/g, m => \`-\${m.toLowerCase()}\`);`,
    `}`,
    ``,
    `/**`,
    ` * Returns the CSS custom property reference for any design token.`,
    ` * Atlassian-aligned: category is dropped from the var name.`,
    ` *`,
    ` * в”Ђв”Ђ Color в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ`,
    ` * token('color.bg.primary')           в†’ var(--bg-primary)`,
    ` * token('color.text.primary')         в†’ var(--text-primary)`,
    ` * token('color.icon.success')         в†’ var(--icon-success)`,
    ` * token('color.border.primary')       в†’ var(--border-primary)`,
    ` * token('color.brand.primary')        в†’ var(--brand-primary)`,
    ` * token('color.ext.success-subtler')  в†’ var(--ext-success-subtler)`,
    ` * token('color.blue.500')             в†’ var(--color-blue-500)`,
    ` *`,
    ` * в”Ђв”Ђ Spacing в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ`,
    ` * token('spacing.xs')   в†’ var(--space-xs)`,
    ` * token('spacing.md')   в†’ var(--space-md)`,
    ` *`,
    ` * в”Ђв”Ђ Radius / Shadow в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ`,
    ` * token('radius.sm')              в†’ var(--radius-sm)`,
    ` * token('shadow.elevation.md')    в†’ var(--shadow-elevation-md)`,
    ` *`,
    ` * в”Ђв”Ђ Typography в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ`,
    ` * token('typography.fontFamily.sans')     в†’ var(--typography-font-family-sans)`,
    ` * token('typography.fontSize.sm')         в†’ var(--typography-font-size-sm)`,
    ` * token('typography.heading.h1')          в†’ var(--typography-heading-h1)`,
    ` * token('typography.body.regular')        в†’ var(--typography-body-regular)`,
    ` *`,
    ` * в”Ђв”Ђ Motion / Z-Index в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ`,
    ` * token('motion.duration.fast')  в†’ var(--duration-fast)`,
    ` * token('motion.easing.ease')    в†’ var(--easing-ease)`,
    ` * token('zIndex.modal')          в†’ var(--z-modal)`,
    ` *`,
    ` * в”Ђв”Ђ With fallback в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ`,
    ` * token('color.bg.primary', '#fff') в†’ var(--bg-primary, #fff)`,
    ` */`,
    `export function token(path: TokenPath, fallback?: string): string {`,
    `  const varName = \`--\${_pathToVarName(path)}\`;`,
    `  return fallback ? \`var(\${varName}, \${fallback})\` : \`var(\${varName})\`;`,
    `}`,
    ``,
    `/**`,
    ` * Raw CSS variable name вЂ” no var() wrapper.`,
    ` * Use for setProperty(), CSS-in-JS that wraps var() itself, or calc().`,
    ` *`,
    ` * cssVar('color.bg.primary')              в†’ '--bg-primary'`,
    ` * cssVar('typography.fontFamily.sans')    в†’ '--typography-font-family-sans'`,
    ` * element.style.setProperty(cssVar('color.bg.primary'), '#0057b7');`,
    ` */`,
    `export function cssVar(path: TokenPath): string {`,
    `  return \`--\${_pathToVarName(path)}\`;`,
    `}`,
    ``,
    `/**`,
    ` * Wraps a token in calc().`,
    ` *`,
    ` * tokenCalc('spacing.md', '* 2')    в†’ 'calc(var(--space-md) * 2)'`,
    ` * tokenCalc('spacing.sm', '+ 4px')  в†’ 'calc(var(--space-sm) + 4px)'`,
    ` */`,
    `export function tokenCalc(path: TokenPath, expression: string): string {`,
    `  return \`calc(\${token(path)} \${expression})\`;`,
    `}`,
    ``,
  ].join('\n');

  writeFileSync(`${webSrcPath}token.ts`, out, 'utf-8');
}
