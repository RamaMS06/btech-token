import { mkdirSync, writeFileSync } from 'fs';
import { ROOT, hexToArgb, toPascalCase, toYaml } from '../utils.js';
import type { ResolvedTokenMap, TypeScaleEntry } from '../token-loader.js';
import { generateDartSemanticColorGroup, generateTokensMeta } from './flutter-color.js';

const HEADER = '// AUTO-GENERATED by @btech/design-system — do not edit manually.\n// Run `pnpm generate` to regenerate from tokens/.\n\n// ignore_for_file: lines_longer_than_80_chars\n';

/** Emit GoogleFonts.getFont(...) lines for a single TextStyle field. */
function dartGetFont(fam: string, entry: TypeScaleEntry, varName: string): string[] {
  const lines: string[] = [];
  const hasExtra = entry.withTextColor || entry.fontStyle || entry.textDecoration;
  if (!hasExtra) {
    lines.push(
      `  final TextStyle ${varName} = GoogleFonts.getFont('${fam}',`,
      `      fontSize: ${entry.fontSize}, fontWeight: FontWeight.w${entry.fontWeight}, height: ${entry.lineHeightPx} / ${entry.fontSize});`,
    );
  } else {
    lines.push(`  final TextStyle ${varName} = GoogleFonts.getFont('${fam}',`);
    lines.push(`      fontSize: ${entry.fontSize}, fontWeight: FontWeight.w${entry.fontWeight}, height: ${entry.lineHeightPx} / ${entry.fontSize},`);
    const extras: string[] = [];
    if (entry.fontStyle === 'italic')         extras.push(`      fontStyle: FontStyle.italic,`);
    if (entry.textDecoration === 'underline') extras.push(`      decoration: TextDecoration.underline,`);
    if (entry.withTextColor)                  extras.push(`      color: BTechColor.text.neutral,`);
    const last = extras.pop()!.replace(/,$/, ');');
    lines.push(...extras, last);
  }
  return lines;
}

export function generateFlutterFiles(data: ResolvedTokenMap): void {
  const DART_SRC = `${ROOT}/platforms/flutter/lib/src`;
  const DART_LIB = `${ROOT}/platforms/flutter/lib`;

  // ── color/ ─────────────────────────────────────────────────────────────────
  const colorDir = `${DART_SRC}/color`;
  mkdirSync(colorDir, { recursive: true });

  // shades.color.dart — uses MaterialColor so Flutter theming works natively.
  // Access: BTechColor.shades.blue          → primary swatch color (500)
  //         BTechColor.shades.blue[400]     → specific shade via MaterialColor
  //         BTechColor.shades.blue.shade700 → via MaterialColor getter
  {
    const L = [HEADER, "import 'package:flutter/material.dart';\n"];
    const groupNames: string[] = [];

    for (const [group, shades] of Object.entries(data.coreColors)) {
      groupNames.push(group);
      const entries = Object.entries(shades)
        .filter(([k]) => k !== '0') // key 0 (white) is excluded from MaterialColor map
        .sort((a, b) => Number(a[0]) - Number(b[0]));

      // Pick the 500 shade as the MaterialColor primary; fall back to the middle entry
      const primaryHex = (shades['500'] ?? shades[Object.keys(shades).sort((a, b) => Number(a) - Number(b))[Math.floor(Object.keys(shades).length / 2)]]) as string;

      const mapEntries = entries.map(([shade, hex]) =>
        `      ${shade}: Color(${hexToArgb(hex)}),`
      ).join('\n');

      L.push(`/// ${toPascalCase(group)} color swatch — BTechColor.shades.${group}[500]`);
      L.push(`final MaterialColor btechShades${toPascalCase(group)} = MaterialColor(`);
      L.push(`  ${hexToArgb(primaryHex)},`);
      L.push(`  <int, Color>{`);
      L.push(mapEntries);
      L.push(`  },`);
      L.push(`);\n`);
    }

    L.push('/// Primitive color palette as MaterialColor swatches.');
    L.push('/// Access: BTechColor.shades.blue        → primary 500');
    L.push('///         BTechColor.shades.blue[400]   → shade 400');
    L.push('///         BTechColor.shades.green.shade700');
    L.push('class BTechShadesColor {');
    L.push('  const BTechShadesColor();');
    for (const group of groupNames) {
      L.push(`  MaterialColor get ${group} => btechShades${toPascalCase(group)};`);
    }
    L.push('}\n');
    writeFileSync(`${colorDir}/shades.color.dart`, L.join('\n') + '\n');
  }

  generateDartSemanticColorGroup(colorDir, 'text',       data.semanticColors.text,       HEADER);
  generateDartSemanticColorGroup(colorDir, 'icon',       data.semanticColors.icon,       HEADER);
  generateDartSemanticColorGroup(colorDir, 'background', data.semanticColors.background, HEADER);
  generateDartSemanticColorGroup(colorDir, 'stroke',     data.semanticColors.stroke,     HEADER);

  // color.token.dart
  writeFileSync(`${colorDir}/color.token.dart`, [
    HEADER,
    "import 'text.color.dart';",
    "import 'icon.color.dart';",
    "import 'background.color.dart';",
    "import 'stroke.color.dart';",
    "import 'shades.color.dart';\n",
    '/// Semantic color tokens — use context.btechColor for tenant-aware access.',
    '///   BTechColor.text.neutral           → Color (#111827)',
    '///   BTechColor.text.neutral.subtle    → Color (#6B7280)',
    '///   BTechColor.icon.neutral           → Color (#374151)',
    '///   BTechColor.background.primary     → Color (#15803D)',
    '///   BTechColor.stroke.neutral         → Color (#E5E7EB)',
    '///   BTechColor.shades.green[500]      → Color (#22C55E)',
    'abstract class BTechColor {',
    '  static const BTechTextColor       text       = BTechTextColor();',
    '  static const BTechIconColor       icon       = BTechIconColor();',
    '  static const BTechBackgroundColor background = BTechBackgroundColor();',
    '  static const BTechStrokeColor     stroke     = BTechStrokeColor();',
    '  static const BTechShadesColor     shades     = BTechShadesColor();',
    '}\n',
  ].join('\n') + '\n');

  // color.dart barrel
  writeFileSync(`${colorDir}/color.dart`, [
    "export 'shades.color.dart';",
    "export 'text.color.dart';",
    "export 'icon.color.dart';",
    "export 'background.color.dart';",
    "export 'stroke.color.dart';",
    "export 'color.token.dart';",
    '',
  ].join('\n'));

  generateTokensMeta(colorDir, 'color', data.semanticColors);

  // ── spacing/ ────────────────────────────────────────────────────────────────
  const spacingDir = `${DART_SRC}/spacing`;
  mkdirSync(spacingDir, { recursive: true });

  const spacingLines = [HEADER, '/// Spacing tokens. Access: BTechSpacing.md', 'abstract class BTechSpacing {'];
  for (const [name, value] of Object.entries(data.spacing)) {
    spacingLines.push(`  static const double ${name} = ${parseFloat(String(value).replace('px', ''))};`);
  }
  spacingLines.push('}\n');
  writeFileSync(`${spacingDir}/spacing.token.dart`, spacingLines.join('\n') + '\n');
  writeFileSync(`${spacingDir}/spacing.dart`, "export 'spacing.token.dart';\n");
  writeFileSync(`${spacingDir}/tokens.meta.yaml`, toYaml({
    category: 'spacing',
    figma: {
      collection: 'BTech/Spacing',
      tokens: Object.keys(data.spacing).map(name => ({
        path: `spacing.${name}`,
        figmaVariable: `Spacing/${toPascalCase(name)}`,
      })),
    },
  }) + '\n');

  // ── radius/ ─────────────────────────────────────────────────────────────────
  const radiusDir = `${DART_SRC}/radius`;
  mkdirSync(radiusDir, { recursive: true });

  const radiusLines = [HEADER, '/// Radius tokens. Access: BTechRadius.interactive', 'abstract class BTechRadius {'];
  for (const [name, value] of Object.entries(data.radius.core)) {
    radiusLines.push(`  static const double ${name} = ${parseFloat(String(value).replace('px', ''))};`);
  }
  radiusLines.push('');
  radiusLines.push('  // Semantic radius aliases');
  for (const [name, value] of Object.entries(data.radius.semantic)) {
    radiusLines.push(`  static const double ${name} = ${parseFloat(String(value).replace('px', ''))};`);
  }
  radiusLines.push('}\n');
  writeFileSync(`${radiusDir}/radius.token.dart`, radiusLines.join('\n') + '\n');
  writeFileSync(`${radiusDir}/radius.dart`, "export 'radius.token.dart';\n");
  const allRadiusKeys = [...Object.keys(data.radius.core), ...Object.keys(data.radius.semantic)];
  writeFileSync(`${radiusDir}/tokens.meta.yaml`, toYaml({
    category: 'radius',
    figma: {
      collection: 'BTech/Radius',
      tokens: allRadiusKeys.map(name => ({ path: `radius.${name}`, figmaVariable: `Radius/${toPascalCase(name)}` })),
    },
  }) + '\n');

  // ── typography/ ─────────────────────────────────────────────────────────────
  const typoDir = `${DART_SRC}/typography`;
  mkdirSync(typoDir, { recursive: true });

  const sansFam = (data.typography.fontFamilies.sans ?? 'Inter').split(',')[0].trim().replace(/'/g, '');

  // heading.dart
  {
    const scales = data.typography.typeScale.heading;
    const first = Object.values(scales)[0] ?? { fontSize: 35, fontWeight: 700, lineHeightPx: 40 };
    const L = [HEADER,
      "import 'package:flutter/material.dart';",
      "import 'package:google_fonts/google_fonts.dart';",
      "import '../color/color.token.dart';\n",
      '/// Heading text styles (h1 – h4).',
      '/// For tenant-aware font switching, use context.btechFont.heading instead.',
      '///',
      '/// ```dart',
      `/// Text('Title',      style: BTechFont.heading.h1)`,
      `/// Text('Card title', style: BTechFont.heading.h3)`,
      '/// ```',
      'class BTechFontHeading extends TextStyle {',
      `  BTechFontHeading()`,
      `      : super(`,
      `          fontSize: ${first.fontSize},`,
      `          height: ${first.lineHeightPx} / ${first.fontSize},`,
      `          fontFamily: GoogleFonts.getFont('${sansFam}',`,
      `            fontWeight: FontWeight.w${first.fontWeight}).fontFamily,`,
      `        );\n`,
    ];
    for (const [name, entry] of Object.entries(scales)) {
      L.push(`  /// ${name.toUpperCase()} — ${entry.fontSize}px · w${entry.fontWeight} · lineHeight: ${entry.lineHeightPx}/${entry.fontSize} = ${(entry.lineHeightPx / entry.fontSize).toFixed(3)}`);
      L.push(...dartGetFont(sansFam, entry, name));
      L.push('');
    }
    L.push('}\n');
    writeFileSync(`${typoDir}/heading.dart`, L.join('\n') + '\n');
  }

  // subheading.dart
  {
    const scales = data.typography.typeScale.subheading;
    const first = Object.values(scales)[0] ?? { fontSize: 16, fontWeight: 700, lineHeightPx: 20 };
    const L = [HEADER,
      "import 'package:flutter/material.dart';",
      "import 'package:google_fonts/google_fonts.dart';",
      "import '../color/color.token.dart';\n",
      '/// Subheading text styles (h5 – h7).',
      '/// For tenant-aware font switching, use context.btechFont.subheading instead.',
      '///',
      '/// ```dart',
      `/// Text('Section', style: BTechFont.subheading.h5)`,
      `/// Text('Label',   style: BTechFont.subheading.h6)`,
      '/// ```',
      'class BTechFontSubHeading extends TextStyle {',
      `  BTechFontSubHeading()`,
      `      : super(`,
      `          fontSize: ${first.fontSize},`,
      `          height: ${first.lineHeightPx} / ${first.fontSize},`,
      `          fontFamily: GoogleFonts.getFont('${sansFam}',`,
      `            fontWeight: FontWeight.w${first.fontWeight}).fontFamily,`,
      `        );\n`,
    ];
    for (const [name, entry] of Object.entries(scales)) {
      L.push(`  /// ${name.toUpperCase()} — ${entry.fontSize}px · w${entry.fontWeight} · lineHeight: ${entry.lineHeightPx}/${entry.fontSize} = ${(entry.lineHeightPx / entry.fontSize).toFixed(3)}`);
      L.push(...dartGetFont(sansFam, entry, name));
      L.push('');
    }
    L.push('}\n');
    writeFileSync(`${typoDir}/subheading.dart`, L.join('\n') + '\n');
  }

  // body.dart
  {
    const scales = data.typography.typeScale.body;
    const baseEntry = scales['base'] ?? { fontSize: 12, fontWeight: 500, lineHeightPx: 16 };
    const L = [HEADER,
      "import 'package:flutter/material.dart';",
      "import 'package:google_fonts/google_fonts.dart';",
      "import '../color/color.token.dart';\n",
      '/// Body text styles.',
      '/// For tenant-aware font switching, use context.btechFont.body instead.',
      '///',
      '/// ```dart',
      `/// Text('Body copy',  style: BTechFont.body)`,
      `/// Text('Bold label', style: BTechFont.body.bold)`,
      `/// Text('Small hint', style: BTechFont.body.small)`,
      '/// ```',
      'class BTechFontBody extends TextStyle {',
      `  BTechFontBody()`,
      `      : super(`,
      `          fontSize: ${baseEntry.fontSize},`,
      `          fontWeight: FontWeight.w${baseEntry.fontWeight},`,
      `          fontFamily: GoogleFonts.getFont('${sansFam}',`,
      `            fontSize: ${baseEntry.fontSize}, fontWeight: FontWeight.w${baseEntry.fontWeight},`,
      `            color: BTechColor.text.neutral,`,
      `            height: ${baseEntry.lineHeightPx} / ${baseEntry.fontSize}).fontFamily,`,
      `        );\n`,
    ];
    for (const [name, entry] of Object.entries(scales)) {
      L.push(`  /// ${name} — ${entry.fontSize}px · w${entry.fontWeight} · lineHeight: ${entry.lineHeightPx}/${entry.fontSize} = ${(entry.lineHeightPx / entry.fontSize).toFixed(3)}`);
      L.push(...dartGetFont(sansFam, entry, name));
      L.push('');
    }
    L.push('}\n');
    writeFileSync(`${typoDir}/body.dart`, L.join('\n') + '\n');
  }

  // font.token.dart
  {
    const L = [HEADER, "import 'package:flutter/material.dart';", "import 'heading.dart';", "import 'subheading.dart';", "import 'body.dart';\n"];
    L.push('/// Primitive font family tokens.');
    L.push('abstract class BTechFontFamily {');
    for (const [name, value] of Object.entries(data.typography.fontFamilies)) {
      L.push(`  static const String ${name} = '${String(value).split(',')[0].trim().replace(/'/g, '')}';`);
    }
    L.push('}\n');

    L.push('/// Primitive font size tokens.');
    L.push('abstract class BTechFontSize {');
    for (const [name, value] of Object.entries(data.typography.fontSizes)) {
      const safeName = /^[0-9]/.test(name) ? `s${name}` : name;
      L.push(`  static const double ${safeName} = ${parseFloat(String(value).replace('px', ''))};`);
    }
    L.push('}\n');

    L.push('/// Primitive font weight tokens.');
    L.push('abstract class BTechFontWeight {');
    for (const [name, value] of Object.entries(data.typography.fontWeights)) {
      L.push(`  static const FontWeight ${name} = FontWeight.w${Number(value)};`);
    }
    L.push('}\n');

    L.push('/// Primitive line height tokens (ratio, not pixels).');
    L.push('abstract class BTechLineHeight {');
    for (const [name, value] of Object.entries(data.typography.lineHeights)) {
      L.push(`  static const double ${name} = ${Number(value)};`);
    }
    L.push('}\n');

    L.push('/// Root static font namespace — always uses the default sans font family.');
    L.push('/// For tenant-aware fonts in widgets, prefer [context.btechFont].');
    L.push('abstract class BTechFont {');
    L.push('  static final BTechFontHeading    heading    = BTechFontHeading();');
    L.push('  static final BTechFontSubHeading subheading = BTechFontSubHeading();');
    L.push('  static final BTechFontBody       body       = BTechFontBody();');
    L.push('}\n');
    writeFileSync(`${typoDir}/font.token.dart`, L.join('\n') + '\n');
  }

  // typography.dart barrel
  writeFileSync(`${typoDir}/typography.dart`, [
    "export 'heading.dart';",
    "export 'subheading.dart';",
    "export 'body.dart';",
    "export 'font.token.dart';",
    "export 'font_registry.dart';",
    '',
  ].join('\n'));

  // tokens.meta.yaml
  const typoMetaTokens: Array<{ path: string; figmaVariable: string }> = [];
  for (const [group, props] of Object.entries(data.typography.semantic)) {
    for (const prop of Object.keys(props)) {
      typoMetaTokens.push({
        path: `typography.${group}.${prop}`,
        figmaVariable: `Typography/${toPascalCase(group)}/${toPascalCase(prop)}`,
      });
    }
  }
  writeFileSync(`${typoDir}/tokens.meta.yaml`, toYaml({ category: 'typography', figma: { collection: 'BTech/Typography', tokens: typoMetaTokens } }) + '\n');

  // ── btech_tokens.dart root barrel ───────────────────────────────────────────
  writeFileSync(`${DART_LIB}/btech_tokens.dart`, [
    '/// btech_tokens — Multi-tenant design tokens for Flutter (BTech Design System).',
    '///',
    '/// Each tenant gets its own isolated package that extends [BtechToken].',
    '/// Register [BTechTokenExtension] once in MaterialApp; then access tokens',
    '/// anywhere via [BuildContext.btechColor], [BuildContext.btechRadius], and',
    '/// [BuildContext.btechFont] — no repeated imports or instance references.',
    '///',
    '/// ```yaml',
    '/// # pubspec.yaml of consuming app (git dependency)',
    '/// dependencies:',
    '///   btech_tokens_bspace:',
    '///     git:',
    '///       url: https://dev.azure.com/buma/.../_git/btech-ds',
    '///       path: packages/tokens/platforms/flutter/tenants/bspace',
    '///       ref: main',
    '/// ```',
    '///',
    '/// ```dart',
    '/// // main.dart — setup once',
    "/// import 'package:btech_tokens_bspace/btech_tokens_bspace.dart';",
    "/// import 'package:btech_tokens/btech_tokens.dart';",
    '///',
    '/// MaterialApp(',
    '///   theme: ThemeData(',
    '///     extensions: [BTechTokenExtension(token: BtechToken.instance)],',
    '///   ),',
    '/// )',
    '///',
    '/// // In any widget — no import of tenant package needed',
    "/// context.btechColor.background.primary   // Color(0xFF0066CC)",
    "/// context.btechColor.text.primary         // Color(0xFFFFFFFF)",
    "/// context.btechRadius.interactive         // 6.0",
    "/// context.btechFont.sans                  // 'Inter'",
    '/// ```',
    'library btech_tokens;',
    '',
    "export 'src/color/color.dart';",
    "export 'src/spacing/spacing.dart';",
    "export 'src/radius/radius.dart';",
    "export 'src/typography/typography.dart';",
    "export 'src/btech_token.dart';",
    "export 'src/tenant.dart';",
    "export 'src/theme.dart';",
    "export 'src/context.dart';",
    '',
  ].join('\n'));
}
