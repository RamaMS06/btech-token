  L.push('@dataclass(frozen=True, slots=True)');
  L.push('class _BTechFontFamilyClass:');
  if (Object.keys(families).length === 0) L.push('    pass');
  for (const k of Object.keys(families)) L.push(`    ${k}: str`);
  L.push('');

  L.push('@dataclass(frozen=True, slots=True)');
