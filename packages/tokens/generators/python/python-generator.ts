  L.push('class _BTechFontClass:');
  L.push('    family: _BTechFontFamilyClass');
  L.push('    size: _BTechFontSizeClass');
  L.push('    weight: _BTechFontWeightClass');
  L.push('    line_height: _BTechLineHeightClass');
